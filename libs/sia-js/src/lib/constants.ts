// BLOCKS_PER_HOUR is the number of blocks expected to be mined per hour.
export const BLOCKS_PER_HOUR = 6

// BLOCKS_PER_DAY is the number of blocks expected to be mined per day.
export const BLOCKS_PER_DAY = 24 * BLOCKS_PER_HOUR

// BLOCKS_PER_WEEK is the number of blocks expected to be mined per week.
export const BLOCKS_PER_WEEK = 7 * BLOCKS_PER_DAY

// BLOCKS_PER_MONTH is the number of blocks expected to be mined per month.
export const BLOCKS_PER_MONTH = 30 * BLOCKS_PER_DAY

// BLOCKS_PER_YEAR is the number of blocks expected to be mined per year.
export const BLOCKS_PER_YEAR = 365 * BLOCKS_PER_DAY

// BYTES_IN_TERABYTE is the number of bytes in terabyte in decimal system used in sia.
export const BYTES_IN_TERABYTE = 1e12
